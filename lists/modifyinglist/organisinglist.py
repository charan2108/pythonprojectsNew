#sorting
cars = ['ferrari', 'benz', 'Astro', 'ford']
cars.sort()
print(cars)

#reverse sorting
cars = ['ferrari', 'benz', 'Astro', 'ford']
cars.sort(reverse=True)
print(cars)

#temp sorting
print("THe orginal lis tIs")
print(cars)
print("\n sorted list is")
print(sorted(cars))
